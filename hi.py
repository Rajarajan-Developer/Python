row=int(input("Enter the no of row :"))
col=int(input("Enter the no of column :"))
print("Give matrix 1 elements :")
m1=[[int(input()) for i in range(row)]for j in range(col)]
print("Give matrix 2 elements :")
m2=[[int(input()) for i in range(row)]for j in range(col)]
print("Matrix 1 elements :")
for i in range(row):
    for j in range(col):
        print(m1[i][j],end="   ")
    print()
print("Matrix 2 elements :")
for i in range(row):
    for j in range(col):
        print(m2[i][j],end="   ")
    print()
result=[[0 for i in range(row)]for j in range(col)]
print("Matrix 1 +Matrix 2 :")
for i in range(row):
    for j in range(col):
        result=m1[i][j]+m2[i][j]
        print(result,end="   ")
    print()
